                            // TYPE CASTING
                            // Syntax of type casting is 
                            //    (type) variable
#include<stdio.h>

int main()
{
    int a=33;
    printf("The value of a is %d \n",a);
    printf("The value of a is %f",(float) a);   //Here white type casting we have converted a into float for that particular argument.
    return 0;
}
